import math

def luas_persegi(s):
    luas= s*s
    print('luas persegi adalah', luas)
    
def jajar_genjang(a,t):
    luas= a*t
    print('luas jajar genjang adalah', luas)
    
def segitiga(a,t):
    luas= (1/2)*a*t
    print('luas segitiga adalah', luas)
    
def lingkaran(r):
    luas = 3.14 * r * r
    print('luas lingkaran adalah', luas)
    
def trapesium(a,b,t):
    luas = (1/2) * (a + b) * t
    print('luas trapesium adalah', luas)